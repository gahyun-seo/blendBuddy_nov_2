class Drink {
  final String drinkName;
  final String drinkBrand;
  final String drinkInfo;
  final String drinkSize;
  final String image;
  final int sodium;
  final int protein;
  final int saturatedFat;
  final int sugars;
  final int caffeine;
  final int calories;

  Drink({
    required this.drinkName,
    required this.drinkBrand,
    required this.drinkInfo,
    required this.drinkSize,
    required this.image,
    required this.sodium,
    required this.protein,
    required this.saturatedFat,
    required this.sugars,
    required this.caffeine,
    required this.calories,
  });
}
